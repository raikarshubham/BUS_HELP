function searchBus() {
    const fromLocation = document.getElementById('from-location').value.trim().toLowerCase();
    const toLocation = document.getElementById('to-location').value.trim().toLowerCase();
    const resultsDiv = document.getElementById('bus-list');
    const resultsSection = document.getElementById('results');

    if (!fromLocation || !toLocation) {
        alert("Please select both starting location and destination.");
        return;
    }

    // Filter buses
    const filteredBuses = busData
        .filter(bus => bus.from === fromLocation && bus.to === toLocation)
        .sort((a, b) => new Date('1970/01/01 ' + a.departure) - new Date('1970/01/01 ' + b.departure));

    resultsDiv.innerHTML = ""; // Clear previous results
    resultsSection.style.display = "block"; // Make results visible

    if (filteredBuses.length > 0) {
        filteredBuses.forEach(bus => {
            const busInfo = `
                <div class="bus-info">
                    <h3>${bus.busName} (${bus.type})</h3>
                    <p><strong>Departure:</strong> ${bus.departure}</p>
                </div>
            `;
            resultsDiv.innerHTML += busInfo;
        });
    } else {
        resultsDiv.innerHTML = "<p>No buses found for the selected route.</p>";
    }
}
